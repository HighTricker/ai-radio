"""normalize_for_tts 归一化规则测试：年份逐字化，排除年代/月日/数量。"""
import pytest

from services.tts_text import normalize_for_tts


@pytest.mark.parametrize("src,want", [
    # 单个年份 → 逐字
    ("2020年", "二零二零年"),
    ("1969年", "一九六九年"),
    ("那是1997年的夏天", "那是一九九七年的夏天"),
    # 年份里只动年，月/日不碰
    ("1969年7月20日", "一九六九年7月20日"),
    ("2008年8月8日开幕", "二零零八年8月8日开幕"),
    # 年份范围：符号连接号读作「到」
    ("2018-2020年", "二零一八到二零二零年"),
    ("2018~2020年", "二零一八到二零二零年"),
    ("2018－2020年", "二零一八到二零二零年"),  # 全角连接号
    # 范围：中文连接词保留
    ("2018到2020年", "二零一八到二零二零年"),
    ("2018至2020年", "二零一八至二零二零年"),
    # 「X年到Y年」两边都带年 → 各自逐字
    ("2018年到2020年", "二零一八年到二零二零年"),
    # 年份后接普通字也照转
    ("2020年后再说", "二零二零年后再说"),
    ("到了2100年", "到了二一零零年"),
    # ⭐ 真凶回归：数字与「年」之间带空格（大模型很常这么写）
    ("2020 年", "二零二零年"),                       # 半角空格
    ("2019　年", "二零一九年"),                  # 全角空格 U+3000
    ("2019 年", "二零一九年"),                  # 不换行空格 U+00A0
    ("那是1997 年的夏天", "那是一九九七年的夏天"),
    ("十点半的地铁在2020 年冬天", "十点半的地铁在二零二零年冬天"),
    ("2018 - 2020 年", "二零一八到二零二零年"),        # 带空格的范围
])
def test_year_converted(src, want):
    assert normalize_for_tts(src) == want


@pytest.mark.parametrize("src", [
    # 「年代」不能逐字（该读九十年代，不是九零年代）
    "90年代",
    "80年代的歌",
    "2000年代",
    "2000 年代",   # 带空格也不能碰（仍是「年代」）
    "90 年代",
    # 非 4 位数字的时间/数量，TTS 默认就念对，别碰
    "5年前",
    "20年老友",
    "3首歌",
    "第2首",
    "19:30准时",
    "8月15日",
    # 纯数字无「年」不碰（避免误伤金额/编号）
    "共2020条",
    "编号1997",
    # 5 位数字不碰
    "共12020年",
])
def test_left_untouched(src):
    assert normalize_for_tts(src) == src


def test_empty_and_none_safe():
    assert normalize_for_tts("") == ""
    assert normalize_for_tts(None) is None


def test_multiple_years_in_one_sentence():
    src = "从1990年到2000年，再到2020年"
    want = "从一九九零年到二零零零年，再到二零二零年"
    assert normalize_for_tts(src) == want


def test_display_text_semantics():
    """归一化不该动非年份内容，句子其余部分完全保留。"""
    src = "欢迎收听电台，现在是2024年，为你播放3首好歌。"
    want = "欢迎收听电台，现在是二零二四年，为你播放3首好歌。"
    assert normalize_for_tts(src) == want
